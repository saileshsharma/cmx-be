package com.cb.th.claims.cmx.enums.fnol;

public enum FNOLSeverity {
        MINOR,      // Small damages (e.g., scratches, dents, no injuries)
        MODERATE,   // Noticeable damages, possible medical attention, drivable vehicle
        MAJOR,      // Severe damage, likely requires towing, possible hospitalization
        TOTAL_LOSS, // Vehicle is beyond repair or written off
        CATASTROPHIC // Multi-vehicle accidents, fatalities, large-scale incidents
}